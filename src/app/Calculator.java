package app;

/**
 * 计算器类，提供基本的加法和减法功能。
 * @author 胡邦民
 * @version 1.0
 */
public class Calculator {

    /**
     * 执行加法运算
     * @param a 第一个加数
     * @param b 第二个加数
     * @return a + b 的计算结果
     */
    public int add(int a, int b) {
        return a + b;
    }

    /**
     * 执行减法运算
     * @param a 被减数
     * @param b 减数
     * @return a - b 的计算结果
     */
    public int subtract(int a, int b) {
        return a - b;
    }
}
